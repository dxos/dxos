/**
 * Generates the literal `--color-<hue>-<role>` tokens in src/css/theme/styles.css from the tone
 * table below. The tokens must be literal custom properties on disk (not expanded at PostCSS time)
 * because several pipelines read the theme without our Vite plugin chain — notably Tailwind's
 * `@reference "…/main.css"` loader used by the lit-* packages, and tailwindcss's design-system
 * loader used in tests.
 *
 * Edit the table here, then run `pnpm --filter @dxos/ui-theme gen-theme`.
 *
 * Tones are `[light, dark]`; a single value collapses to one `var()`. Every hue shares BASE unless
 * listed in OVERRIDES. ALIASES map a semantic name onto a source hue (forwarding every role).
 */

import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HUES = [
  'neutral',
  'red',
  'orange',
  'amber',
  'yellow',
  'lime',
  'green',
  'emerald',
  'teal',
  'cyan',
  'sky',
  'blue',
  'indigo',
  'violet',
  'purple',
  'fuchsia',
  'pink',
  'rose',
];

// Default tones shared by every hue.
const BASE = {
  fill: [600, 700],
  'fill-hover': [700, 800],
  surface: [300, 600],
  foreground: [700, 100],
  text: [700, 500],
  border: [600, 600],
};

// Per-hue overrides / additions (none currently).
const OVERRIDES = {};

// Semantic names → source hue.
const ALIASES = {
  primary: 'blue',
  secondary: 'green',
  info: 'cyan',
  success: 'emerald',
  warning: 'amber',
  error: 'rose',
};

const EMIT_ORDER = ['fill', 'fill-hover', 'surface', 'foreground', 'text', 'border'];

const rolesFor = (hue) => EMIT_ORDER.filter((role) => (OVERRIDES[hue] ?? {})[role] ?? BASE[role]);

const toValue = (hue, [light, dark = light]) =>
  light === dark
    ? `var(--color-${hue}-${light})`
    : `light-dark(var(--color-${hue}-${light}), var(--color-${hue}-${dark}))`;

const block = (entries) => {
  const lines = [];
  let prev = null;
  for (const [prop, value] of entries) {
    const name = prop.replace(/^--color-/, '').replace(/-(?:fill-hover|fill|surface|foreground|text|border)$/, '');
    if (prev && name !== prev) {
      lines.push('');
    }
    lines.push(`  ${prop}: ${value};`);
    prev = name;
  }
  return lines.join('\n');
};

const aliasEntries = Object.entries(ALIASES).flatMap(([name, hue]) =>
  rolesFor(hue).map((role) => [`--color-${name}-${role}`, `var(--color-${hue}-${role})`]),
);

const hueEntries = HUES.flatMap((hue) =>
  rolesFor(hue).map((role) => [`--color-${hue}-${role}`, toValue(hue, (OVERRIDES[hue] ?? {})[role] ?? BASE[role])]),
);

const output = `/**
 * Color styles.
 *
 * Hue/role tokens: each hue exposes fill / fill-hover / surface / foreground / text / border.
 * \`foreground\` is the on-color for a colored fill/surface; \`text\` is the hue as standalone
 * colored text on the neutral background. Semantic names (primary, error, …) alias a source hue.
 *
 * GENERATED by scripts/gen-hue-roles.mjs (\`pnpm --filter @dxos/ui-theme gen-theme\`).
 * Edit the tone table in that script and re-run; do not hand-edit the tokens below.
 */

@theme {
  /*
   * Semantic — each name aliases a source hue.
   */

${block(aliasEntries)}

  /*
   * Hue.
   */

${block(hueEntries)}
}
`;

const target = resolve(dirname(fileURLToPath(import.meta.url)), '../src/css/theme/styles.css');
writeFileSync(target, output);
// eslint-disable-next-line no-console
console.log(`Wrote ${aliasEntries.length + hueEntries.length} tokens to ${target}`);
