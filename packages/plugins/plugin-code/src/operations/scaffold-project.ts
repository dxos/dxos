import * as Effect from 'effect/Effect';

import { Operation } from '@dxos/compute';
import { Database, Obj, Ref } from '@dxos/echo';
import { trim } from '@dxos/util';

import { SourceFile } from '#types';

import { ScaffoldProject } from './definitions';

type ScaffoldFile = { path: string; content: string };

const buildScaffold = (packageName: string): ScaffoldFile[] => [
  {
    path: 'package.json',
    content:
      JSON.stringify(
        {
          name: packageName,
          version: '0.1.0',
          private: true,
          type: 'module',
          main: 'src/plugin.ts',
        },
        null,
        2,
      ) + '\n',
  },
  {
    path: 'src/plugin.ts',
    content:
      trim`
        //
        // Copyright 2026 DXOS.org
        //

        import { definePlugin } from '@dxos/app-framework';

        export const plugin = definePlugin({
          meta: {
            id: '${packageName}',
            name: '${packageName}',
          },
          ready: async () => {
            // Initialize plugin here.
          },
        });
      ` + '\n',
  },
  {
    path: 'README.md',
    content:
      trim`
        # ${packageName}

        Generated by the Coder blueprint.
      ` + '\n',
  },
];

const handler: Operation.WithHandler<typeof ScaffoldProject> = ScaffoldProject.pipe(
  Operation.withHandler(
    Effect.fn(function* ({ project, name }) {
      const code = yield* Database.load(project);
      const packageName = name ?? code.name ?? 'my-plugin';
      const fileRefs = code.files ?? [];

      const existing = new Map<string, SourceFile.SourceFile>();
      for (const ref of fileRefs) {
        const file = yield* Database.load(ref);
        existing.set(file.path, file);
      }

      const additions: { ref: Ref.Ref<SourceFile.SourceFile>; size: number; path: string }[] = [];
      const updates: { path: string; size: number }[] = [];

      for (const { path, content } of buildScaffold(packageName)) {
        const found = existing.get(path);
        if (found) {
          const text = yield* Database.load(found.content);
          Obj.update(text, (text) => {
            (text as Obj.Mutable<typeof text>).content = content;
          });
          updates.push({ path, size: content.length });
        } else {
          const file = SourceFile.make({ path, content });
          const added = yield* Database.add(file);
          additions.push({ ref: Ref.make(added), size: content.length, path });
        }
      }

      if (additions.length > 0) {
        Obj.update(code, (code) => {
          const next = [...(code.files ?? []), ...additions.map((entry) => entry.ref)];
          (code as Obj.Mutable<typeof code>).files = next;
        });
      }

      const files = [...updates, ...additions.map(({ path, size }) => ({ path, size }))].sort((a, b) =>
        a.path.localeCompare(b.path),
      );
      return { files };
    }),
  ),
);

export default handler;
