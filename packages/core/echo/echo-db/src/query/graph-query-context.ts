export type GraphQueryContextProps = {
  // TODO(dmaretskyi): Make async.
  onStart: () => void;
  onStop: () => void;
};

/**
 * Query data source.
 * Implemented by a space or a remote agent.
 * Each query has a separate instance.
 */
export interface QuerySource {
  // TODO(dmaretskyi): Update info?
  changed: Event<void>;

  // TODO(dmaretskyi): Make async.
  open(ctx: Context): void;

  // TODO(dmaretskyi): Make async.
  close(ctx: Context): void;

  /**
   * Synchronous query.
   */
  getResults(ctx: Context): QueryResult.EntityEntry[];

  /**
   * One-shot query.
   */
  run(ctx: Context, query: QueryAST.Query): Promise<QueryResult.EntityEntry[]>;

  /**
   * Set the filter and trigger continuous updates.
   */
  update(ctx: Context, query: QueryAST.Query): void;
}

/**
 * Aggregates multiple query sources.
 */
export class GraphQueryContext implements QueryContext {
  private readonly _sources = new Set<QuerySource>();

  private _query?: QueryAST.Query = undefined;

  private _ctx?: Context = undefined;

  public changed = new Event<void>();

  constructor(private readonly _params: GraphQueryContextProps) {}

  get sources(): ReadonlySet<QuerySource> {
    return this._sources;
  }

  start(ctx: Context) {
    this._ctx = new Context();
    this._params.onStart();
    for (const source of this._sources) {
      source.open(this._ctx);
      if (this._query) {
        source.update(this._ctx, this._query);
      }

      // Subscribing after `update` means that we will intentionally skip any `changed` events generated by update.
      source.changed.on(this._ctx, () => {
        this.changed.emit();
      });
    }
  }

  stop(ctx: Context) {
    void this._ctx?.dispose();
    for (const source of this.sources) {
      source.close(ctx);
    }
    this._params.onStop();
  }

  getResults(ctx: Context): QueryResult.EntityEntry[] {
    if (!this._query) {
      return [];
    }
    return Array.from(this._sources).flatMap((source) => source.getResults(ctx));
  }

  async run(
    ctx: Context,
    query: QueryAST.Query,
    { timeout = 30_000 }: QueryResult.RunOptions = {},
  ): Promise<QueryResult.EntityEntry[]> {
    const runTasks = [...this._sources.values()].map(async (s) => {
      try {
        log('run query', {
          resolver: Object.getPrototypeOf(s).constructor.name,
        });
        const results = await asyncTimeout<QueryResult.EntityEntry[]>(s.run(ctx, query), timeout);
        log('run query results', {
          resolver: Object.getPrototypeOf(s).constructor.name,
          count: results.length,
        });
        return results;
      } catch (err) {
        log('run query error', {
          resolver: Object.getPrototypeOf(s).constructor.name,
          error: err,
        });
        throw err;
      }
    });
    if (runTasks.length === 0) {
      return [];
    }
    const mergedResults = (await Promise.all(runTasks)).flatMap((r) => r ?? []);
    return mergedResults;
  }

  update(ctx: Context, query: QueryAST.Query): void {
    this._query = query;
    for (const source of this._sources) {
      source.update(ctx, query);
    }
  }

  addQuerySource(ctx: Context, querySource: QuerySource) {
    this._sources.add(querySource);
    if (this._ctx != null) {
      querySource.changed.on(this._ctx, () => {
        this.changed.emit();
      });
    }
    if (this._query) {
      querySource.update(ctx, this._query);
    }
  }
}

/**
 * Queries objects from the local working set.
 */
export class SpaceQuerySource implements QuerySource {
  public readonly changed = new Event<void>();

  private _ctx: Context = new Context();
  private _query: QueryAST.Query | undefined = undefined;
  private _results?: QueryResult.EntityEntry<Obj.Any>[] = undefined;

  constructor(private readonly _database: EchoDatabaseImpl) {}

  get spaceId() {
    return this._database.spaceId;
  }

  get spaceKey() {
    return this._database.spaceKey;
  }

  open(ctx: Context): void {}

  close(ctx: Context) {
    this._results = undefined;
    void this._ctx.dispose().catch(() => {});
  }

  private _onUpdate = (updateEvent: ItemsUpdatedEvent) => {
    if (!this._query) {
      return;
    }

    prohibitSignalActions(() => {
      // TODO(dmaretskyi): Could be optimized to recompute changed only to the relevant space.
      const changed = updateEvent.itemsUpdated.some(({ id: objectId }) => {
        const core = this._database.coreDatabase.getObjectCoreById(this._ctx, objectId, {
          load: false,
        });

        const trivial = isSimpleSelectionQuery(this._query!);
        if (!trivial || trivial.hasQueues) {
          return false;
        }

        const { filter, options } = trivial;

        return (
          !this._results ||
          this._results.find((result) => result.id === objectId) ||
          (core && this._filterCore(this._ctx, core, filter, options))
        );
      });

      if (changed) {
        this._results = undefined;
        this.changed.emit();
      }
    });
  };

  async run(ctx: Context, query: QueryAST.Query): Promise<QueryResult.EntityEntry<Obj.Unknown>[]> {
    if (!this._isValidSourceForQuery(ctx, query)) {
      return [];
    }

    const simple = isSimpleSelectionQuery(query);
    if (!simple || simple.hasQueues) {
      return [];
    }

    const { filter, options } = simple;
    const results: QueryResult.EntityEntry<Obj.Any>[] = [];
    if (isObjectIdFilter(filter)) {
      results.push(
        ...(
          await this._database.coreDatabase.batchLoadObjectCores(
            ctx,
            (filter as QueryAST.FilterObject).id as ObjectId[],
          )
        )
          .filter(Predicate.isNotUndefined)
          .filter((core) => this._filterCore(ctx, core, filter, options))
          .map((core) => this._mapCoreToResult(ctx, core)),
      );
    }

    prohibitSignalActions(() => {
      results.push(...this._queryWorkingSet(ctx, filter, options));
    });

    // Dedup
    const map = new Map<string, QueryResult.EntityEntry<Obj.Unknown>>();
    for (const result of results) {
      map.set(result.id, result);
    }

    return [...map.values()];
  }

  getResults(ctx: Context): QueryResult.EntityEntry<Obj.Unknown>[] {
    if (!this._query) {
      return [];
    }

    const trivial = isSimpleSelectionQuery(this._query);
    if (!trivial || trivial.hasQueues) {
      return [];
    }

    const { filter, options } = trivial;

    if (!this._results) {
      prohibitSignalActions(() => {
        this._results = this._queryWorkingSet(ctx, filter, options);
      });
    }

    return this._results!;
  }

  update(ctx: Context, query: QueryAST.Query): void {
    if (!this._isValidSourceForQuery(ctx, query)) {
      this._query = undefined;
      return;
    }

    void this._ctx.dispose().catch(() => {});
    this._ctx = new Context();
    this._query = query;

    this._database.coreDatabase._updateEvent.on(this._ctx, this._onUpdate);

    this._results = undefined;
    this.changed.emit();
  }

  /**
   * Queries from already loaded objects.
   */
  private _queryWorkingSet(
    ctx: Context,
    filter: QueryAST.Filter,
    options: QueryAST.QueryOptions | undefined,
  ): QueryResult.EntityEntry<Obj.Unknown>[] {
    const filteredCores = isObjectIdFilter(filter)
      ? (filter as QueryAST.FilterObject)
          .id!.map((id) => this._database.coreDatabase.getObjectCoreById(ctx, id, { load: true }))
          .filter(Predicate.isNotUndefined)
          .filter((core) => this._filterCore(ctx, core, filter, options))
      : this._database.coreDatabase.allObjectCores(ctx).filter((core) => this._filterCore(ctx, core, filter, options));

    return filteredCores.map((core) => this._mapCoreToResult(ctx, core));
  }

  private _isValidSourceForQuery(ctx: Context, query: QueryAST.Query): boolean {
    const targetSpaces = getTargetSpacesForQuery(query);
    // Disabled by spaces filter.
    if (targetSpaces.length > 0 && !targetSpaces.includes(this.spaceId)) {
      return false;
    }

    return true;
  }

  private _mapCoreToResult(ctx: Context, core: ObjectCore): QueryResult.EntityEntry<Obj.Unknown> {
    return {
      id: core.id,
      result: this._database.getObjectById(core.id, { deleted: true }),
      match: { rank: 1 },
      resolution: {
        source: 'local',
        time: 0,
      },
    };
  }

  private _filterCore(
    ctx: Context,
    core: ObjectCore,
    filter: QueryAST.Filter,
    options: QueryAST.QueryOptions | undefined,
  ): boolean {
    return (
      filterCoreByDeletedFlag(ctx, core, options) &&
      filterMatchObject(filter, {
        id: core.id,
        doc: core.getObjectStructure(ctx),
        spaceId: this.spaceId,
      })
    );
  }
}

const isObjectIdFilter = (filter: QueryAST.Filter) => {
  return filter.type === 'object' && filter.id !== undefined && filter.id.length > 0;
};

const filterCoreByDeletedFlag = (ctx: Context, core: ObjectCore, options: QueryAST.QueryOptions | undefined): boolean => {
  switch (options?.deleted) {
    case undefined:
    case 'exclude':
      return !core.isDeleted(ctx);
    case 'include':
      return true;
    case 'only':
      return core.isDeleted(ctx);
  }
};
