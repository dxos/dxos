import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

import { analyzeMutations } from './automerge-mutations.js';
import { analyzeDocumentSizes } from './automerge-size.js';
import { openDatabase } from './db.js';
import { formatBytes, formatMs } from './format.js';

/**
 * Build a markdown report for Automerge maintainers.
 *
 * @param {object} input
 * @param {ReturnType<typeof analyzeDocumentSizes>} input.size
 * @param {ReturnType<typeof analyzeMutations>} input.mutations
 * @param {string} input.dbPath
 * @param {string} input.binPath
 */
export const formatEscalationReport = ({ size, mutations, dbPath, binPath }) => {
  const lines = [];
  const nl = () => lines.push('');

  lines.push('# Automerge document escalation report');
  nl();
  lines.push('Generated by `@dxos/composer-forensics` from an offline DXOS Composer SQLite extract.');
  nl();
  lines.push('## Document');
  lines.push(`- **document id:** \`${size.documentId}\``);
  lines.push(`- **source database:** \`${dbPath}\``);
  lines.push(`- **binary artifact:** \`${binPath}\``);
  lines.push(
    `- **chunks:** ${size.chunkCount} (${size.snapshotCount} snapshot, ${size.incrementalCount} incremental) — merged in StorageSubsystem order`,
  );
  lines.push(`- **objectMeta rows:** ${size.objectMetaRows}`);
  nl();

  lines.push('## Size comparison');
  lines.push(`| Metric | Value |`);
  lines.push(`|--------|-------|`);
  lines.push(
    `| Combined binary (loadIncremental input) | ${formatBytes(size.combinedBytes)} (${size.combinedBytes.toLocaleString()} bytes) |`,
  );
  lines.push(
    `| Reified JSON (toJS + JSON.stringify) | ${formatBytes(size.jsonBytes)} (${size.jsonBytes.toLocaleString()} bytes) |`,
  );
  lines.push(`| Binary / JSON ratio | ${size.binToJsonRatio.toFixed(2)}x |`);
  nl();

  lines.push('## Load performance');
  lines.push(`| Step | Time |`);
  lines.push(`|------|------|`);
  lines.push(`| loadIncremental | ${formatMs(size.loadMs)} |`);
  lines.push(`| toJS | ${formatMs(size.toJsMs)} |`);
  lines.push(`| JSON.stringify | ${formatMs(size.stringifyMs)} |`);
  nl();

  lines.push('## Automerge stats');
  lines.push(`- **changes:** ${size.numChanges.toLocaleString()}`);
  lines.push(`- **ops:** ${size.numOps.toLocaleString()}`);
  lines.push(`- **actors:** ${size.numActors}`);
  if (mutations.docStats?.cargoPackageVersion) {
    lines.push(
      `- **automerge wasm:** ${mutations.docStats.cargoPackageName} ${mutations.docStats.cargoPackageVersion}`,
    );
  }
  lines.push(`- **ops / MiB binary:** ${Math.round(size.opsPerMiB).toLocaleString()}`);
  if (size.numChanges > 0) {
    lines.push(`- **ops / change (avg):** ${Math.round(size.numOps / size.numChanges).toLocaleString()}`);
  }
  nl();

  lines.push('## Reified state summary');
  lines.push(`- **top-level keys:** ${mutations.reified.topLevelKeys.join(', ') || '(none)'}`);
  lines.push(`- **objects:** ${mutations.reified.objectCount}`);
  for (const summary of mutations.reified.objectSummaries) {
    if (summary.hasTagsIndex) {
      lines.push(
        `- **${summary.objectId}**${summary.name ? ` (${summary.name})` : ''}: TagIndex \`tags\` — ${summary.tagKeyCount} tag keys, ${summary.tagIdCount} tagged object ids (largest array: ${summary.largestTagArray})`,
      );
    } else {
      lines.push(`- **${summary.objectId}**: data keys [${summary.dataKeys.join(', ')}]`);
    }
  }
  nl();

  lines.push('## Chunk growth');
  lines.push(`| Type | Bytes | Δ ops | Δ changes | Load |`);
  lines.push(`|------|-------|-------|-----------|------|`);
  for (const row of mutations.chunkGrowth) {
    lines.push(
      `| ${row.type} | ${formatBytes(row.bytes)} | ${row.deltaOps.toLocaleString()} | ${row.deltaChanges} | ${formatMs(row.loadMs)} |`,
    );
  }
  nl();

  if (mutations.changeOps) {
    lines.push('## Change op breakdown');
    lines.push(`- **decoded ops:** ${mutations.changeOps.totalDecodedOps.toLocaleString()}`);
    lines.push(
      `- **ops/change:** min ${mutations.changeOps.opsPerChange.min}, p50 ${mutations.changeOps.opsPerChange.p50.toLocaleString()}, p95 ${mutations.changeOps.opsPerChange.p95.toLocaleString()}, max ${mutations.changeOps.opsPerChange.max.toLocaleString()}`,
    );
    lines.push(
      `- **changes >1k ops:** ${mutations.changeOps.opsPerChange.over1k}, **>10k ops:** ${mutations.changeOps.opsPerChange.over10k}`,
    );
    lines.push('');
    lines.push('| Action | Count | % |');
    lines.push('|--------|-------|---|');
    for (const [action, count] of Object.entries(mutations.changeOps.actionCounts).sort((a, b) => b[1] - a[1])) {
      const pct = mutations.changeOps.actionPercent[action] ?? 0;
      lines.push(`| ${action} | ${count.toLocaleString()} | ${pct.toFixed(1)}% |`);
    }
    nl();
  }

  if (mutations.hypotheses.length > 0) {
    lines.push('## Observed pattern / hypothesis');
    for (const note of mutations.hypotheses) {
      lines.push(`- ${note}`);
    }
    nl();
  }

  lines.push('## Reproduce load');
  lines.push('```js');
  lines.push("import { readFileSync, writeFileSync } from 'node:fs';");
  lines.push("import { init, loadIncremental, stats } from '@automerge/automerge';");
  lines.push('');
  lines.push(`const bytes = readFileSync('${binPath}');`);
  lines.push('const start = performance.now();');
  lines.push('const doc = loadIncremental(init(), bytes);');
  lines.push('console.log(performance.now() - start, stats(doc));');
  lines.push('```');
  nl();
  lines.push('## Notes for maintainers');
  lines.push(
    '- Binary is concatenated snapshot + incremental chunk bytes exactly as stored in `automerge_chunks` (automerge-repo `StorageSubsystem.loadDoc` order).',
  );
  lines.push('- Reified JSON is the current materialized document (`Automerge.toJS`), not the on-disk encoding.');
  lines.push('- This extract is from production DXOS Composer user data; handle as confidential.');
  lines.push('- Please redact paths if sharing publicly.');

  return lines.join('\n');
};

/**
 * Write merged `.bin` + markdown report for Automerge maintainer escalation.
 *
 * @param {string} dbPath
 * @param {string} documentId
 * @param {{ outDir?: string, quiet?: boolean, skipChangeOps?: boolean }} [options]
 */
export const writeEscalationBundle = (dbPath, documentId, options = {}) => {
  const outDir = options.outDir ?? '/tmp/composer-forensics/escalation';
  mkdirSync(outDir, { recursive: true });
  const binPath = join(outDir, `${documentId}.bin`);
  const reportPath = join(outDir, `${documentId}-report.md`);

  const size = analyzeDocumentSizes(dbPath, documentId);
  writeFileSync(binPath, size.merged);

  const db = openDatabase(dbPath);
  const mutations = analyzeMutations(db, documentId, size.merged, {
    includeChangeOps: !options.skipChangeOps,
  });

  const report = formatEscalationReport({ size, mutations, dbPath, binPath });
  writeFileSync(reportPath, report, 'utf8');

  const result = {
    documentId,
    binPath,
    reportPath,
    size,
    mutations,
  };

  if (!options.quiet) {
    console.log('Automerge escalation bundle');
    console.log('=========================');
    console.log(`document_id: ${documentId}`);
    console.log('');
    console.log(`  ${binPath}`);
    console.log(`  ${reportPath}`);
    console.log('');
    console.log(
      `binary: ${formatBytes(size.combinedBytes)}  json: ${formatBytes(size.jsonBytes)}  ratio: ${size.binToJsonRatio.toFixed(2)}x`,
    );
    console.log(`load: ${formatMs(size.loadMs)}  changes: ${size.numChanges}  ops: ${size.numOps.toLocaleString()}`);
    if (mutations.hypotheses[0]) {
      console.log('');
      console.log('Top hypothesis:');
      console.log(`  ${mutations.hypotheses[0]}`);
    }
  }

  return result;
};
